def restore_vietnamese_diacritics(text: str) -> str:
    """
    Restore Vietnamese diacritics on undiacritized text.
    Uses a simple dictionary-based approach for common words.
    """
    if not text:
        return text
    
    # Remove spaces and convert to lowercase for matching
    words = text.split()
    corrected = []
    
    for word in words:
        # Try to find exact match first
        lower_word = word.lower()
        
        # Look for partial matches (undiacritized form)
        best_match = None
        for undiacritized, diacritized_forms in VIETNAMESE_CORRECTIONS.items():
            if lower_word == undiacritized or lower_word.startswith(undiacritized):
                best_match = diacritized_forms[0]  # Take first (most common) form
                break
        
        if best_match:
            # Preserve original casing if first letter was uppercase
            if word and word[0].isupper():
                corrected.append(best_match[0].upper() + best_match[1:])
            else:
                corrected.append(best_match)
        else:
            corrected.append(word)
    
    return ' '.join(corrected)

