def _pil_to_gray(image: Image.Image) -> np.ndarray:
    if image.mode != "L":
        image = image.convert("L")
    return np.array(image)


def _to_tensor(gray_img: np.ndarray, target_h: int = 32, max_w: int = 512) -> torch.Tensor:
    resized = resize_keep_aspect(gray_img, height=target_h, max_width=max_w)
    normalized = normalize_for_model(resized)
    padded = pad_width((255 - (normalized * 255)).astype(np.uint8), max_w)
    normalized = (255 - padded).astype(np.float32) / 255.0
    return torch.from_numpy(normalized).unsqueeze(0).unsqueeze(0)


def _enhance(gray: np.ndarray) -> np.ndarray:
    """Enhanced image preprocessing with advanced techniques"""
    # Use new enhancer module for better quality
    enhanced = ImageEnhancer.enhance_image(gray)
    return enhanced


@dataclass
class OCRResult:
    text: str
    source: Optional[str]
    candidates: List[Dict[str, str]]
    success: bool
    pages: int = 1
