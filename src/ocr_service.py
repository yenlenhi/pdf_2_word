    def train_from_zip(
        self,
        zip_bytes: bytes,
        epochs: int = 3,
        batch_size: int = 4,
        lr: float = 1e-4,
        use_synthetic: bool = True,
        progress_callback=None,
        save_path: Optional[str] = None,
    ) -> Dict[str, float]:
        with tempfile.TemporaryDirectory() as tmp:
            tmp_path = Path(tmp)
            with zipfile.ZipFile(io.BytesIO(zip_bytes)) as archive:
                archive.extractall(tmp_path)

            images_dir = None
            root_dir = None
            labels_file = None
            train_file = None
            val_file = None
            for path in tmp_path.rglob("*"):
                if path.is_dir() and path.name.lower() == "images":
                    images_dir = path
                    root_dir = path.parent
                if path.is_file():
                    name = path.name.lower()
                    if name == "labels.txt":
                        labels_file = path
                    elif name == "train_set.txt":
                        train_file = path
                    elif name == "validation_set.txt":
                        val_file = path

            if images_dir is None:
                raise ValueError("Zip must contain an images/ folder")

            if (train_file is None or val_file is None) and labels_file is None:
                raise ValueError("Zip must include labels.txt or train/validation splits")

            if root_dir is None:
                root_dir = images_dir.parent

            if train_file is None or val_file is None:
                lines = [line.strip() for line in labels_file.read_text(encoding="utf-8").splitlines() if line.strip()]
                if not lines:
                    raise ValueError("labels.txt is empty")
                split_idx = max(1, int(len(lines) * 0.9))
                train_lines = lines[:split_idx]
                val_lines = lines[split_idx:] or lines[-1:]
                train_file = root_dir / "train_set.txt"
                val_file = root_dir / "validation_set.txt"
                train_file.write_text("\n".join(train_lines), encoding="utf-8")
                val_file.write_text("\n".join(val_lines), encoding="utf-8")

            dataset_root = str(root_dir)
            return self.train_on_directory(
                dataset_root=dataset_root,
                epochs=epochs,
                batch_size=batch_size,
                lr=lr,
                use_synthetic=use_synthetic,
                progress_callback=progress_callback,
                save_path=save_path,
                train_split=train_file.name,
                val_split=val_file.name,
            )


